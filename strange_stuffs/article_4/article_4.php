 						<h1 class="entry-title"><a href="article_3.php" rel="bookmark">一些看过的推理作品推荐</a></h1>
					</header> 
					<div class="entry-content"> 
						<p>
							特别喜欢看推理类的作品（或者说侦探类吧）是因为一方面很有趣啦，看起来读起来都很有感觉，另一方面就是多多少少能体会逻辑思考的乐趣，体会命运的无常，人与人之间的纠结隔阂啦blahblah这里就不多扯了。想着自己也算看过不少自认为还算不错的侦探作品吧，这里就只罗列一些个人最喜欢的作品，然后有些太有名的也不写了比如福尔摩斯、柯南之类的，就算一个个人的小推荐吧。有兴趣的自己看看呗嘤嘤嘤～
						</p>
						<h3>
							书籍
						</h3>
						<h5>
							埃勒里·奎因
						</h5>
						<p>
							奎因兄弟是我个人最喜欢的推理小说作家，这里摘抄一下豆瓣的描述：
						</p>
						<blockquote>
							埃勒里·奎因（Ellery Queen）是曼弗雷德·班宁顿·李（Manfred Bennington Lee，1905年-1971年）和弗雷德里克·丹奈（Frederic Dannay，1905年-1982年）合用的笔名，侦探推理小说史上承前启后的经典作家，美国推理小说代名词。曼弗雷德·班宁顿·李与弗雷德里克·丹奈是一对表兄弟，他们开创了合作撰写推理小说成功的先例。在他们的小说作品中，埃勒里·奎因也是小说主人公，一位侦探小说作家兼超级侦探。从1929年到1971年，埃勒里·奎因共发表数十部推理小说，其中9部“国名系列”作品和4部“悲剧系列”作品被认为是古典解谜推理小说最高水平的代表。
						</blockquote>
						<p>
							奎因的所谓“公平的推理小说”，也即日本所谓本格推理小说，我认为是最经典的、最有代表性的作品。这类作品将线索一一罗列，让读者与作品中的侦探进行公平的对决。奎因兄弟的作品中还有标志性的“挑战读者”环节，挑战观众是否能从已给的线索中看出事件的真相。<br>个人最喜欢的奎因的作品有：
						</p>
						<ul>
							<li>《希腊棺材之谜》：绝对的本格推理经典，不可不读的侦探小说，最跌宕起伏，难以置信的逻辑和故事，首推首推首推。</li>
							<li>《Y的悲剧》：最悲伤的故事，推理非常严谨，结局非常惊人，强烈推荐。</li>
							<li>《X的悲剧》：个人认为最有趣的故事，老雷恩魅力十足，值得一读的优秀作品。</li>
						</ul>
						<h5>
							阿加莎·克里斯蒂
						</h5>
						<p>
							加婆就不用详细介绍了，毕竟太有名了，她也有诸多属于家喻户晓的小说，例如《东方快车谋杀》、《无人生还》、《尼罗河上的惨案》。都是很优秀的古典推理作品，但是个人不太喜欢这种英式风格啊感觉有点磨叽，看着需要不少耐心。<br>这里推荐《罗杰疑案》和《ABC谋杀》，都是不是特别有名吧但超棒的书，读起来很有意思。
						</p>
						<h5>
							东野圭吾
						</h5>
						<p>东野圭吾也超有名的啦，《白夜行》、《嫌疑人X的献身》都算是那种大家都知道的书。有些书的话我个人不是特别喜欢，因为感觉不属于那种本格的范围了，可能有些人会喜欢吧。这里推荐几本我很喜欢的不是很出名的：</p>
						<ul>
							<li>《流星之绊》：喜爱喜爱喜爱，最早读的东野圭吾的小说，很暖的结局。</li>
							<li>《黑笑小说》《毒笑小说》《怪笑小说》：当初是看名字很有意思才看的XD，不过这三本短篇小说集确实很有意思。</li>
							<li>《回廊亭杀人案》：有点兼具社会派和本格派的作品吧，感觉算是我能接受的类型了，还算挺喜欢的，安利安利。</li>
						</ul>
						<h5>
							岛田庄司
						</h5>
						<p>
							岛田的作品读的不多，但是感觉都很好看，这里推荐一个是《占星术杀人魔法》，还有一个是《斜屋犯罪》，感觉他的作品有那种比较强烈的风格（有些有点恶心讲道理），较为独特吧，所以可能在海外没有东野那么火吧。
						</p>
						<h5>
							其他的一些
						</h5>
						<p>
							比如钱德勒的《漫长的告别》也算经典吧，但是个人不是很喜欢。<br>还有一些新一点的欧美作品我没怎么读过，这里就不做推荐了。
						</p>
						<h3>
							电视剧
						</h3>
						<h5>
							摩斯探长前传/Endeavors
						</h5>
						<p>
							某人特别喜爱的作品吧然后推荐给我的。结果我发现简直太对胃口了，缓慢的节奏，阴郁的天气反衬着探长契而不舍，追求真相的精神和行为，简直perfect！经典的歌剧和BGM，永远合身的西装加风衣搭配，还有可爱的贵人星期四大大和英格兰那美丽的田园风光，还要什么自行车？？？！！！快去看吧孩子。
						</p>
						<h5>
							谋杀/Killing
						</h5>
						<p>
							美版的摩斯探长？类似吧。疑点重重，层层推进，一个案件讲两季就问您还有谁？？？感觉这个剧情才像是警察正常的破案速度23333。看完怀疑西雅图天天下雨系列，女主蜜汁越看越好看，欧范十足，五星力推。
						</p>
						<h5>
							基本演绎法/Elementary
						</h5>
						<p>
							刚播出的时候挺喜欢的剧，女版华人花生真可爱啊啃手指～但是第后面开始感觉缺少适当的主线剧情，故事缺乏主要矛盾没有导致感觉变成了福尔摩斯天天刷日常任务，故弃了，但是前两季还是挺不错的，嗯。
						</p>
						<h5>
							九号秘事/Inside NO.9
						</h5>
						<p>
							个人特别特别特别喜欢的一部英剧，其实不算推理类剧吧，就当打点擦边球嘻嘻嘻。每一季的每一集都是一个不同的故事，然后主要演员就那么两个，他们还负责编剧什么的，很有才。故事大多比较黑色幽默，也有些比较恐怖，看完多少会有点后背发凉，细思恐极，安利。
						</p>
						<h5>
							其他
						</h5>
						<p>
							什么神探夏洛克啦，真探啦都挺出名的，没看过的也可以看看哟～
						</p>
						<h3>
							电影
						</h3>
						<p>
							电影的话感觉挺多的，但是把视角专门放在一个侦探上的很少，毕竟篇幅有限。有些看起来是侦探作品的例如大侦探福尔摩斯也不能算那种严格意义上的推理吧。这里就随意推几个有意思的悬疑电影吧：
						</p>
						<ul>
							<li>热血警探/Hot Fuzz：英式作品中的少数，各种搞怪各种酷炫，但是黑色幽默以及荒诞的剧情还是很British的。</li>
							<li>罪恶之家/An Inspector Calls：BBC拍的电影，剧情很有意思，但是故事多多少少有点另类，不适合所有观众。</li>
							<li>囚徒/Prisoners：狼叔狼叔狼叔！重要的事要多说几遍。总体故事还是挺不错的，可能有点拖沓吧，很久前看的了不太记得了。</li>
							<li>杀人回忆/Memories of Murder：经典的电影了吧，真实世界中的案件往往都是这样，没有神探，没有结局，在一片喧嚣中归于平静，沉入了历史的长河，很接地气的剧情。</li>
						</ul>
						<p><br><br>大概就这些吧，以后有时间也会继续写一些其他方面的推荐或者总结的呢～</p>
